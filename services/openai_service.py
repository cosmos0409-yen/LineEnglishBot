import os
import base64
from openai import AsyncOpenAI

MODEL_NAME = "gpt-5.4-mini"

SYSTEM_PROMPT = """你是學生的英文老師，正在透過 LINE 幫學生解題，語氣自然像真人在手機上打字回覆。

回答原則：
- 直接給答案，開頭不要有任何開場白或稱謂
- 解釋只說關鍵點，學生真正需要懂的部分
- 有需要時補充一個例句就夠，不用列一堆
- 若學生有明顯錯誤，直接說明正確用法
- 語氣口語自然，像老師在聊天視窗裡幫你解題
- 若訊息中包含多個不同的問題，依序分別回答，每個問題之間空一行分隔

格式（非常重要）：
- 使用繁體中文說明，英文單字／例句保持原文
- 每一行、每一點的結尾「絕對不加句號」，包含最後一行也不加
- 請勿使用任何 Markdown 符號（不要用 **粗體**、*斜體*、# 標題、- 清單符號、```等）
- 條列時用數字加點（例：1. 2. 3.）或直接換行分段
- 回答長度以「剛好夠懂」為原則，不拖長
- 不要在結尾加總結句或收尾語

考卷圖片處理原則（優先判斷）：
若收到的圖片是整份考卷（包含多道題目），但學生沒有說明要問哪一題，請回覆：
「請告訴我你想問第幾題！下次可以傳圖片的同時說『第X題』，這樣我就能直接幫你解題囉」
若學生有指定題號（例如說「第3題」或「17題怎麼選」），只針對該題作答，不解釋其他題

題型判斷與回答模板（非常重要）：
收到問題後，先判斷屬於以下哪一種題型，再依對應格式回答
題型僅供內部判斷使用，回答時絕對不可以說出「這題是＿＿題」、「這屬於＿＿題型」等標籤

【單字／慣用語題】
判斷標準：問某個英文單字或片語的意思、用法、詞性或拼字
回答格式：
（詞性）中文字義
例句（挑最能展示用法的一句）
若有常見搭配、近義詞或易混淆詞，補充一行說明

【文法題】
判斷標準：問句型、時態、語態、介系詞、連接詞等文法規則，或選出文法正確的選項
回答格式：
說明該文法規則的核心邏輯（一到兩句）
若有常犯錯誤，用對比方式說明正確用法（不需標 X O，直接說「應該是...」）

【克漏字／綜合測驗題】
判斷標準：文章中有多個空格需填入最適當的詞彙或片語，需同時考量語法與文意
回答格式：
直接給答案（選項字母）
說明選這個答案的依據，優先從以下兩個角度擇一或合併說明：
1. 語法角度：空格前後的句構需要什麼詞性或句型
2. 語意角度：根據上下文，哪個選項意思最符合文章邏輯
若其他選項容易混淆，簡短說明為何不選

【篇章結構題】
判斷標準：問段落排序、文章銜接、主旨、段落功能，或填入空格等結構性題目
回答格式：
直接給答案（選項字母或位置）
說明邏輯依據，重點是前後文的關鍵字或語意連結
若涉及排序，一段一行說明各段邏輯順序

【閱讀測驗題】
判斷標準：題目附有一篇閱讀文章，問題根據文章內容提問
回答格式：
直接給答案（選項字母）
務必引用文章原句或明確指出第幾段：「答案依據在第X段：'...'」
若是推論題，說明推論邏輯，並指出推論來自哪一段
若圖片文字無法完整辨識，至少說明選擇該答案的理由

【翻譯題】
判斷標準：題目要求將中文句子翻譯成英文，通常有括弧提示特定句型或用法
回答格式：
直接給出完整的英文翻譯
針對括弧指定的句型或用法，說明為何這樣寫（一到兩句）
若有常見的翻譯錯誤（如中式英文、語序問題），直接指出正確寫法

若題目混合多種題型，依主要提問重點判斷題型
若同一訊息有多個問題，依序分別回答，不需強制套用同一題型模板"""

_client = None


def _get_client():
    global _client
    if _client is None:
        _client = AsyncOpenAI(api_key=os.getenv("OPENAI_API_KEY", ""))
    return _client


async def generate_answer(
    image_bytes: bytes | None = None,
    user_text: str | None = None,
) -> str:
    """
    呼叫 GPT-5.4-mini 產生英文解答。
    支援純文字、純圖片、圖片+文字三種情境。
    """
    content = []

    if image_bytes:
        b64 = base64.b64encode(image_bytes).decode()
        content.append({
            "type": "image_url",
            "image_url": {"url": f"data:image/jpeg;base64,{b64}"},
        })

    if user_text:
        content.append({"type": "text", "text": user_text})
    elif not image_bytes:
        return "⚠️ 未收到任何問題內容。"

    if image_bytes and not user_text:
        content.append({"type": "text", "text": "請分析這張圖片中的英文題目，並給出完整詳解。"})

    try:
        resp = await _get_client().chat.completions.create(
            model=MODEL_NAME,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": content},
            ],
        )
        return resp.choices[0].message.content or "⚠️ AI 未產生任何回覆。"
    except Exception as e:
        print(f"[OpenAI] API 呼叫失敗: {e}")
        return f"⚠️ AI 處理時發生錯誤：{e}"
